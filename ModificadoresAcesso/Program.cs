//No C# temos 5 tipos de modificadores de acesso: public, private, internal, protected e file
//public - o membro pode ser acessado por qualquer outro código no mesmo assembly ou em outro assemblçy que faz referência a ele
//private - o membro pode ser acessado somente pelo código na mesma classe ou struct
//internal - o membro pode ser acessado por qualquer código no mesmo assembly, mas não de outro assembly
//protected - o membro pode ser acessado por qualquer código no assembly no qual ele é declarado ou de uma classe derivada em outro assembly
//file - restringe o escopo e a visibilidade de um tipo de nível superior ao arquivo no qual ele foi declarado

//classes, structs , interfaces e enums possuem internal como visibilidade padrão
//métodos, campos e propriedades possuem visibilidade private por padrão


